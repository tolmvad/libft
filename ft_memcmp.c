#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	t_uch	*u_s1;
	t_uch	*u_s2;
	size_t	i;

	u_s1 = (t_uch *)s1;
	u_s2 = (t_uch *)s2;
	i = 0;
	while (i < n)
	{
		if (u_s1[i] != u_s2[i])
			return (u_s1[i] - u_s2[i]);
		i++;
	}
	return (0);
}
